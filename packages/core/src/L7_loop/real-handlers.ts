function readPRD(azaDir: string): PRD | null {
  for (const f of ['prd.json', 'prd.yaml', 'prd.yml']) {
    const p = path.join(azaDir, f);
    if (fs.existsSync(p)) {
      try {
        const raw = JSON.parse(fs.readFileSync(p, 'utf-8'));
        const parsed = PRDSchema.safeParse(raw);
        if (parsed.success) return parsed.data as PRD;
      } catch {
        continue;
      }
    }
  }
  // Fallback: project-root prd.json
  const root = path.join(azaDir, '..', 'prd.json');
  if (fs.existsSync(root)) {
    try {
      const raw = JSON.parse(fs.readFileSync(root, 'utf-8'));
      const parsed = PRDSchema.safeParse(raw);
      if (parsed.success) return parsed.data as PRD;
    } catch {
      /* ignore */
    }
  }
  return null;
}

async function runCmd(cmd: string, cwd: string): Promise<{ ok: boolean; out: string }> {
  try {
    const { stdout, stderr } = await execAsync(cmd, { cwd, timeout: 120000 });
    return { ok: true, out: stdout };
  } catch (e: any) {
    return { ok: false, out: e?.stdout?.toString?.() || e?.stderr?.toString?.() || String(e) };
  }
}

